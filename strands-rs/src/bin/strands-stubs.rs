use anyhow::Result;
use clap::Parser;
use pyo3_introspection::{introspect_cdylib, module_stub_files};
use std::path::PathBuf;

#[derive(Parser)]
#[clap(about = "Generate Python type stubs from a compiled PyO3 extension")]
struct Args {
    /// Path to the compiled .so/.dylib
    library: PathBuf,
    /// Module name as imported in Python
    #[clap(short, long)]
    module_name: String,
    /// Output directory for .pyi files
    #[clap(short, long, default_value = ".")]
    out_dir: PathBuf,
}

fn main() -> Result<()> {
    let args = Args::parse();
    let module = introspect_cdylib(&args.library, &args.module_name)?;
    let header = format!(
        "# Autogenerated by `{}`. Do not edit.\n\n",
        env!("CARGO_BIN_NAME"),
    );

    std::fs::create_dir_all(&args.out_dir)?;
    for (path, content) in &module_stub_files(&module) {
        let dest = args.out_dir.join(match path.file_name() {
            Some(f) if f == "__init__.pyi" => PathBuf::from(format!("{}.pyi", args.module_name)),
            _ => path.clone(),
        });

        if let Some(parent) = dest.parent() {
            std::fs::create_dir_all(parent)?;
        }
        std::fs::write(&dest, format!("{header}{content}"))?;
        println!("wrote {}", dest.display());
    }

    Ok(())
}
